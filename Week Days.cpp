# include <iostream>
using namespace std ;
int main(){
	
	int x;
	cout<<"enter a number 1 to 7   ";
	cin>>x;
	
	if ( x==1){
		cout<<"monday";
	}
	
	if (x==2){
		cout<<"tuesday";
	}
	
	else if ( x==3){
		cout<<"wednesday";
	}
	
	else if (x==4){
		cout<<"thursday";
	}
	
	else if (x==5){
		cout<<"friday";
	}
	
	else if (x==6){
		cout<<"saturday";
	}
	
	else if (x==7){
		cout<<"sunday";
	}
	
	else{
		cout<<"rong number type";
	}
	
}
